# ფუნქციამ უნდა დააბრუნოს, ლუწია თუ კენტი.

# გამოიძახე ფუნქცია რამდენჯერმე სხვადასხვა არგუმენტებით

def num(number):
    if number % 2 == 0:
        return "even"
    else:
        return "odd"

