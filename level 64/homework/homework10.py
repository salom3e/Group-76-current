# 10)შექმენი ფუნქცია სამი num1 num2 num3.

# ფუნქციამ უნდა დააბრუნოს ამ სამი რიცხვის საშუალო არითმეტიკული.

# გამოიძახე ფუნქცია რამდენჯერმე სხვადასხვა არგუმენტებით

def nums(num1, num2, num3):
    return (num1 + num2 + num3) / 3

print(nums(2, 3, 4))
print(nums(6, 5, 1))
print(nums(7, 8, 6))
print(nums(11, 12, 13))
print(nums(9, 10, 4))

