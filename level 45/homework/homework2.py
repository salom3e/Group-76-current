# კენტ ინდექსზე მდგომი რიცხვები რომელბიც არიან აუცილებლად კენტი. გამოიყენეთ შესაბამისი სიის ფუნქცია და for ციკლი.

nums = [2, 4, 6, 8, 10, 1, 3, 5, 7, 9]

nums_odd = []

for i in range(len(nums)):
    if i % 2 != 0:
        if nums[i] % 2 != 0:
            nums_odd.append(nums[i])
print(nums_odd)
