# არის სიაში - count() ფუნქციის გამოყენებით. სხვა შემთხვევაში append()-ით ჩასვი ის სიაში და დაბეჭდე სია.

a = [5, 5, 7]

user = int(input("enter any number: "))

if user in a:
    print(a.count(user))
else:
    a.append(user)
    print(a)